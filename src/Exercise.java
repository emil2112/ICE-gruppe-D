public class Exercise {
    int sets;
    int reps;
    float weight;
    float restTime;
    String muscleType;

    public Exercise(int sets, int reps, float weight, float restTime, String muscleType){
        this.sets = sets;
        this.reps = reps;
        this.weight = weight;
        this.restTime = restTime;
        this.muscleType = muscleType;
    }
}
